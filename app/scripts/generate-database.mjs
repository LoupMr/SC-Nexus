#!/usr/bin/env node
/**
 * 1. Syncs JSON from ../../Database/ to src/data/ (item folders only; excludes Locations, Ops)
 * 2. Scans src/data and generates database-data.generated.ts
 * Run before build: node scripts/generate-database.mjs
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const APP_ROOT = path.resolve(__dirname, "..");
const DATA_DIR = path.join(APP_ROOT, "src", "data");
const DATABASE_SOURCE = path.join(APP_ROOT, "..", "Database");
const OUT_FILE = path.join(APP_ROOT, "src", "lib", "database-data.generated.ts");

const EXCLUDED = ["ledger.json", "Locations", "Ops"];
const CATEGORY_LABELS = {
  Vehicle_Weaponry: "Ship Weapons",
  Vehicle_Components: "Ship Components",
  Fpsgadgets_Utilities: "FPS Gadgets & Utilities",
  Other: "Miscellaneous",
};
const FILENAME_TO_SUBCATEGORY = {
  WEAPONS: { id: "Weapons", label: "Weapons" },
  MISSILES: { id: "Missiles", label: "Missiles" },
  MISSILERACKS: { id: "MissileRacks", label: "Missile Racks" },
  TURRETS: { id: "Turrets", label: "Turrets" },
  BOMBS: { id: "Bombs", label: "Bombs" },
  BOMBLAUNCHERS: { id: "BombLaunchers", label: "Bomb Launchers" },
  SHIELDS: { id: "Shields", label: "Shields" },
  POWERPLANTS: { id: "PowerPlants", label: "Power Plants" },
  COOLERS: { id: "Coolers", label: "Coolers" },
  QUANTUMDRIVES: { id: "QuantumDrives", label: "Quantum Drives" },
  JUMPDRIVES: { id: "JumpDrives", label: "Jump Drives" },
  FLIGHTBLADES: { id: "FlightBlades", label: "Flight Blades" },
  LIFESUPPORTGENERATORS: { id: "LifeSupport", label: "Life Support" },
  TOOLS: { id: "Tools", label: "Tools" },
  TOOLATTACHMENTS: { id: "ToolAttachments", label: "Tool Attachments" },
  HACKINGCHIPS: { id: "HackingChips", label: "Hacking Chips" },
  MISCELLANOUS: { id: "Miscellaneous", label: "Miscellaneous" },
};

function isExcluded(relPath) {
  return EXCLUDED.some((p) => relPath.includes(p));
}

function getSubcategory(filename) {
  const known = FILENAME_TO_SUBCATEGORY[filename];
  if (known) return known;
  const label = filename.charAt(0).toUpperCase() + filename.slice(1).toLowerCase();
  return { id: filename, label };
}

const entries = [];

function scanDir(dir, prefix = "") {
  const items = fs.readdirSync(dir, { withFileTypes: true });
  for (const item of items) {
    const relPath = path.join(prefix, item.name).replace(/\\/g, "/");
    if (item.isDirectory()) {
      scanDir(path.join(dir, item.name), relPath);
    } else if (item.name.endsWith(".json") && !isExcluded(relPath)) {
      const match = relPath.match(/^([^/]+)\/([^/]+)\.json$/);
      if (match) {
        const [, category, filename] = match;
        const sub = getSubcategory(filename);
        entries.push({ category, filename, subcategoryId: sub.id, subcategoryLabel: sub.label, importPath: relPath });
      }
    }
  }
}

// Sync from Database/ to src/data/ (all folders except Locations, Ops)
if (fs.existsSync(DATABASE_SOURCE)) {
  const skipFolders = ["Locations", "Ops"];
  const itemFolders = fs.readdirSync(DATABASE_SOURCE, { withFileTypes: true })
    .filter((d) => d.isDirectory() && !skipFolders.includes(d.name))
    .map((d) => d.name);
  for (const folder of itemFolders) {
    const srcFolder = path.join(DATABASE_SOURCE, folder);
    const destFolder = path.join(DATA_DIR, folder);
    if (fs.existsSync(srcFolder)) {
      fs.mkdirSync(destFolder, { recursive: true });
      for (const f of fs.readdirSync(srcFolder)) {
        if (f.endsWith(".json") || f.endsWith(".JSON")) {
          const destName = f.endsWith(".JSON") ? f.slice(0, -5) + ".json" : f;
          fs.copyFileSync(path.join(srcFolder, f), path.join(destFolder, destName));
        }
      }
    }
  }
}

if (!fs.existsSync(DATA_DIR)) {
  console.error("Data dir not found:", DATA_DIR);
  process.exit(1);
}
scanDir(DATA_DIR);
if (entries.length === 0) {
  console.error("No JSON files found in", DATA_DIR);
  process.exit(1);
}

const categories = [...new Set(entries.map((e) => e.category))];
const categoryLabels = categories.map((c) => ({
  id: c,
  label: CATEGORY_LABELS[c] ?? c.replace(/_/g, " "),
}));

let out = `// AUTO-GENERATED by scripts/generate-database.mjs - do not edit
// Run: node scripts/generate-database.mjs

`;

entries.forEach((e, i) => {
  const varName = `data${i}`;
  out += `import ${varName} from "@/data/${e.importPath.replace(/\\/g, "/")}";\n`;
});
out += "\n";

out += `export const categories = [
  { id: "all", label: "All Items" },
  ${categoryLabels.map((c) => `{ id: "${c.id}", label: "${c.label}" }`).join(",\n  ")}
] as const;

export const subcategories = [
  ${entries.map((e) => `{ id: "${e.subcategoryId}", label: "${e.subcategoryLabel}", category: "${e.category}" }`).join(",\n  ")}
] as const;

export const dataEntries = [
  ${entries.map((e, i) => `{ category: "${e.category}", subcategory: "${e.subcategoryId}", items: data${i} }`).join(",\n  ")}
] as const;
`;

fs.writeFileSync(OUT_FILE, out, "utf8");
console.log(`Generated ${OUT_FILE} with ${entries.length} data files`);
